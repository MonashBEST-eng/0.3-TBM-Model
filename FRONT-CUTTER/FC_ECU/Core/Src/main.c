#include <stdint.h>

typedef enum {
    STEP_AB = 0, // A+ B-
    STEP_AC,     // A+ C-
    STEP_BC,     // B+ C-
    STEP_BA,     // B+ A-
    STEP_CA,     // C+ A-
    STEP_CB      // C+ B-
} bldc_step_t;

/* ---- DWT microsecond timer ---- */
static inline void dwt_init(void)
{
    CoreDebug->DEMCR |= CoreDebug_DEMCR_TRCENA_Msk;
    DWT->CYCCNT = 0;
    DWT->CTRL |= DWT_CTRL_CYCCNTENA_Msk;
}
static inline uint32_t micros(void)
{
    return (uint32_t)(DWT->CYCCNT / (SystemCoreClock / 1000000U));
}
static inline void delay_us(uint32_t us)
{
    uint32_t t0 = micros();
    while ((micros() - t0) < us) { __NOP(); }
}

/* ---- Phase helpers (TIM1 advanced timer) ----
   phase index: 0=A (CH1/CH1N), 1=B (CH2/CH2N), 2=C (CH3/CH3N)
*/
static inline void phase_enable(uint8_t ph, uint8_t en)
{
    // CC1E/CC1NE are in groups of 4 bits per channel for advanced timers.
    uint32_t shift = 4U * ph;
    uint32_t mask  = (TIM_CCER_CC1E | TIM_CCER_CC1NE) << shift;

    if (en) TIM1->CCER |= mask;
    else    TIM1->CCER &= ~mask;
}

static inline void phase_set_duty(uint8_t ph, uint16_t duty)
{
    switch (ph) {
        case 0: TIM1->CCR1 = duty; break; // A
        case 1: TIM1->CCR2 = duty; break; // B
        case 2: TIM1->CCR3 = duty; break; // C
        default: break;
    }
}

static inline void all_phases_float(void)
{
    // Disable outputs (both high and low) for all phases
    phase_enable(0, 0);
    phase_enable(1, 0);
    phase_enable(2, 0);

    // Set duty = 0
    phase_set_duty(0, 0);
    phase_set_duty(1, 0);
    phase_set_duty(2, 0);
}

/* ---- 6-step commutation using complementary PWM ----
   Strategy:
   - PWM phase: enable outputs, CCRx = duty (high-side PWM, low-side complementary)
   - LOW-ON phase: enable outputs, CCRx = 0  (high-side off, low-side on due to complementary)
   - FLOAT phase: disable outputs
*/
static void bldc_apply_step(bldc_step_t s, uint16_t duty)
{
    // Safe commutation: everything of	f -> small delay -> set new state
    all_phases_float();
    delay_us(5); // extra commutation deadtime (timer deadtime still applies too)

    // shorthand macros
    #define PWM_PHASE(p)  do { phase_enable((p),1); phase_set_duty((p), duty); } while(0)
    #define LOW_ON(p)     do { phase_enable((p),1); phase_set_duty((p), 0); } while(0)
    #define FLOAT(p)      do { phase_enable((p),0); } while(0)

    switch (s) {
        case STEP_AB: PWM_PHASE(0); LOW_ON(1); FLOAT(2); break; // A+ B-
        case STEP_AC: PWM_PHASE(0); LOW_ON(2); FLOAT(1); break; // A+ C-
        case STEP_BC: PWM_PHASE(1); LOW_ON(2); FLOAT(0); break; // B+ C-
        case STEP_BA: PWM_PHASE(1); LOW_ON(0); FLOAT(2); break; // B+ A-
        case STEP_CA: PWM_PHASE(2); LOW_ON(0); FLOAT(1); break; // C+ A-
        case STEP_CB: PWM_PHASE(2); LOW_ON(1); FLOAT(0); break; // C+ B-
        default: break;
    }

    #undef PWM_PHASE
    #undef LOW_ON
    #undef FLOAT
}

