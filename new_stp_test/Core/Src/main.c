  /* USER CODE BEGIN WHILE */
  while (1)
  {
	  // testing go brake sleep back sleep, worked on leds
	  go();
	  HAL_Delay(3000);

	  brake();
	  HAL_Delay(1000);

	  sleep();
	  HAL_Delay(1000);

	  back();
	  HAL_Delay(3000);

	  sleep();
	  HAL_Delay(1000);

	  break;
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }

