void go(){
	if(STP_dir == FORWARD_STANDBY){
	}

	if(STP_dir == BACKWARD_STANDBY){
		  GPIOB -> BSRR = p_pins;
		  GPIOC -> BSRR = (n_pins << 16);

	}
	if(STP_dir == SLEEP_STANDBY){
		  GPIOB -> BSRR = p_pins;

	}


	      HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_1);
		  HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_2);
		  HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_3);
		  HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_4);

		  HAL_TIM_PWM_Start(&htim3, TIM_CHANNEL_1);
		  HAL_TIM_PWM_Start(&htim3, TIM_CHANNEL_2);

		  HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_1);

		  STP_dir = FORWARD;

}

void back(){
	if(STP_dir == FORWARD_STANDBY){
		  GPIOC -> BSRR = n_pins;
		  GPIOB -> BSRR = (p_pins << 16);
	}
	if(STP_dir == SLEEP_STANDBY){
		  GPIOC -> BSRR = n_pins;
	}

	if(STP_dir == BACKWARD_STANDBY){


	}

	      HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_1);
		  HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_2);
		  HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_3);
		  HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_4);

		  HAL_TIM_PWM_Start(&htim3, TIM_CHANNEL_1);
		  HAL_TIM_PWM_Start(&htim3, TIM_CHANNEL_2);

		  HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_1);
		  STP_dir = BACKWARD;

}

void brake(){
	if(STP_dir == FORWARD){
		GPIOC -> BSRR = n_pins;
	}
	if(STP_dir == BACKWARD){
		GPIOB -> BSRR = p_pins;
	}

	STP_dir = BRAKE;
}

void sleep(){
	  HAL_TIM_PWM_Stop(&htim1, TIM_CHANNEL_1);   // master first
	  HAL_TIM_PWM_Stop(&htim2, TIM_CHANNEL_1);
	  HAL_TIM_PWM_Stop(&htim2, TIM_CHANNEL_2);
	  HAL_TIM_PWM_Stop(&htim2, TIM_CHANNEL_3);
	  HAL_TIM_PWM_Stop(&htim2, TIM_CHANNEL_4);

	  HAL_TIM_PWM_Stop(&htim3, TIM_CHANNEL_1);
	  HAL_TIM_PWM_Stop(&htim3, TIM_CHANNEL_2);

	  if (STP_dir == BACKWARD){
		  STP_dir = BACKWARD_STANDBY;
	  }

	  if (STP_dir == FORWARD){
		  STP_dir = FORWARD_STANDBY;
	  	  }
	  if (STP_dir == BRAKE){
		  GPIOC -> BSRR = (n_pins << 16);
		  GPIOB -> BSRR = (p_pins << 16);
		  STP_dir = SLEEP_STANDBY;
	  }
}

void changespeed(uint8_t newspeed){
	// new speed in percentage
	if (newspeed>100 || newspeed<0){
		Error_Handler();
	}

	int new_on =  round(full_cycle*newspeed/100);
	TIM2->CCR1 = new_on;
	TIM2->CCR2 = new_on;
	TIM2->CCR3 = new_on;
	TIM2->CCR4 = new_on;
	TIM3->CCR1 = new_on;
	TIM3->CCR2 = new_on;

}
